public class typecasting {
    public static void main(String[] args) {
        double d=10.999;
        //Explicit typecasting double to int // it is manual
        int  a= (int)d;
        System.out.println(a);
        int b=5;
       //Implicit typecasting int to double // it is automatic
        double f=b;
        System.out.println(f);
    }
   
}
